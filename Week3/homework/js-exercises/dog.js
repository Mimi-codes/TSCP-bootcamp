'use strict'

function calculateDogAge(number) {
let dogYear = 7; //assigns 7 to the declared variable
var age = dogYear * number; //multiplies the value of dogYear by value of number
console.log(`Your doggie is ${age} years old in dog years!`); //logs the doggie's age in dog years
}

calculateDogAge(1);
calculateDogAge(2);
calculateDogAge(3);
